#!/usr/bin/env node
// Embed the remote-install scripts into a TS module the hub Worker can import.
//
// The Cloudflare Worker has no filesystem, so it can't read scripts/ at runtime.
// This reads the two canonical scripts and writes
// packages/hub/src/cf/install-scripts.ts exporting them as string constants.
// Wired as `prebuild`/`predeploy` in packages/hub/package.json so the embedded
// copy can never drift from the source scripts.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");

const sh = readFileSync(join(root, "scripts", "install-remote.sh"), "utf8");
const ps1 = readFileSync(join(root, "scripts", "install-remote.ps1"), "utf8");

const out = `// GENERATED by scripts/embed-install-scripts.mjs — do not edit by hand.
// Source of truth: scripts/install-remote.{sh,ps1}. Re-run the embed script
// (\`bun run embed-scripts\` in packages/hub) after editing either script.

export const INSTALL_SH = ${JSON.stringify(sh)};

export const INSTALL_PS1 = ${JSON.stringify(ps1)};
`;

const dest = join(root, "packages", "hub", "src", "cf", "install-scripts.ts");
writeFileSync(dest, out);
console.log(`[embed-install-scripts] wrote ${dest}`);
